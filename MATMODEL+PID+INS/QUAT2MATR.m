function C=QUAT2MAT(Q)
     Q0=Q(1); Q1=Q(2); Q2=Q(3); Q3=Q(4);
     
     C(1,1) =  Q0*Q0+Q1*Q1-Q2*Q2-Q3*Q3;
	 C(1,2) = (Q1*Q2-Q0*Q3)*2;
	 C(1,3) = (Q0*Q2+Q1*Q3)*2;
     
	 C(2,1) = (Q0*Q3+Q1*Q2)*2;
	 C(2,2) =  Q0*Q0-Q1*Q1+Q2*Q2-Q3*Q3;
	 C(2,3) = (Q2*Q3-Q0*Q1)*2;	 
     
	 C(3,1) = (Q1*Q3-Q0*Q2)*2;
	 C(3,2) = (Q0*Q1+Q2*Q3)*2;
	 C(3,3) = (Q0*Q0-Q1*Q1-Q2*Q2+Q3*Q3);
end